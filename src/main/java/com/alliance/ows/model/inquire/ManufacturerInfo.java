package com.alliance.ows.model.inquire;

public class ManufacturerInfo {
	private String universalManufacturer;
	private String customerManufacturer;
	private String supplierManufacturer;

	public String getUniversalManufacturer() {
		return universalManufacturer;
	}

	public void setUniversalManufacturer(String universalManufacturer) {
		this.universalManufacturer = universalManufacturer;
	}

	public String getCustomerManufacturer() {
		return customerManufacturer;
	}

	public void setCustomerManufacturer(String customerManufacturer) {
		this.customerManufacturer = customerManufacturer;
	}

	public String getSupplierManufacturer() {
		return supplierManufacturer;
	}

	public void setSupplierManufacturer(String supplierManufacturer) {
		this.supplierManufacturer = supplierManufacturer;
	}

}
